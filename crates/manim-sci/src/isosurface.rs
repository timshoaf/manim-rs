//! Isosurface extraction from a [`ScalarField`] via standard **marching cubes**.
//!
//! [`Isosurface`] samples a [`ScalarField`] on a regular grid over an
//! axis-aligned region and polygonises the level set `f(p) = level` into a
//! welded, closed [`TriMesh`]. Vertices are placed by linear interpolation of
//! the field value along each active cube edge; shared cell edges are welded so
//! the result is a proper manifold (correct Euler characteristic). Per-vertex
//! normals come straight from the field's **exact** (automatic-differentiation)
//! gradient — for a signed-distance-like field such as `|p| − 1` they point
//! outward.
//!
//! The 256-entry edge/tri lookup tables ([`EDGE_TABLE`] and [`TRI_TABLE`]) are
//! the classic marching-cubes edge/tri tables after Paul Bourke
//! (<http://paulbourke.net/geometry/polygonise/>), Lorensen & Cline 1987;
//! freely usable.
//!
//! ```
//! use manim_sci::isosurface::Isosurface;
//! use manim_fields::field::{ScalarField, UnaryOp};
//!
//! // Signed-distance field of the unit sphere: f(p) = |p| − 1.
//! let x = ScalarField::coordinate(0);
//! let y = ScalarField::coordinate(1);
//! let z = ScalarField::coordinate(2);
//! let r2 = x.mul(&x).add(&y.mul(&y)).add(&z.mul(&z));
//! let sphere = r2.map(UnaryOp::Sqrt).sub(&ScalarField::constant(1.0));
//!
//! // Small, fast resolution for a doctest.
//! let mesh = Isosurface::new(sphere, 0.0)
//!     .region([-1.5, -1.5, -1.5], [1.5, 1.5, 1.5])
//!     .resolution(12)
//!     .mesh();
//!
//! assert!(!mesh.positions.is_empty());
//! // Every emitted vertex sits (approximately) on the unit sphere.
//! for p in &mesh.positions {
//!     assert!((p.length() - 1.0).abs() < 0.05);
//! }
//! ```

use std::collections::HashMap;

use glam::{DVec3, Vec3};
use manim_core::mesh::{Mesh, TriMesh};
use manim_core::mobject::MobjectId;
use manim_core::scene_state::SceneState;
use manim_fields::field::ScalarField;
