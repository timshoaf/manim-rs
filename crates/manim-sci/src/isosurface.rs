#[cfg(test)]
mod tests {
    use super::*;
    use std::collections::HashSet;

    use manim_fields::field::UnaryOp;

    /// Signed-distance field of the unit sphere: f(p) = |p| − 1.
    fn sphere_field() -> ScalarField {
        let x = ScalarField::coordinate(0);
        let y = ScalarField::coordinate(1);
        let z = ScalarField::coordinate(2);
        let r2 = x.mul(&x).add(&y.mul(&y)).add(&z.mul(&z));
        r2.map(UnaryOp::Sqrt).sub(&ScalarField::constant(1.0))
    }

    /// Torus field: f(p) = (sqrt(x²+y²) − R)² + z² − r².
    fn torus_field(big_r: f64, small_r: f64) -> ScalarField {
        let x = ScalarField::coordinate(0);
        let y = ScalarField::coordinate(1);
        let z = ScalarField::coordinate(2);
        let rho = x.mul(&x).add(&y.mul(&y)).map(UnaryOp::Sqrt);
        let a = rho.sub(&ScalarField::constant(big_r));
        a.mul(&a)
            .add(&z.mul(&z))
            .sub(&ScalarField::constant(small_r * small_r))
    }

    /// χ = V − E + F, where E is the count of unique undirected edges.
    fn euler_characteristic(mesh: &TriMesh) -> i64 {
        let v = mesh.positions.len() as i64;
        let f = (mesh.indices.len() / 3) as i64;
        let mut edges: HashSet<(u32, u32)> = HashSet::new();
        for tri in mesh.indices.chunks_exact(3) {
            let (a, b, c) = (tri[0], tri[1], tri[2]);
            for (i, j) in [(a, b), (b, c), (c, a)] {
                edges.insert((i.min(j), i.max(j)));
            }
        }
        v - edges.len() as i64 + f
    }

    #[test]
    fn sphere_vertices_lie_on_surface() {
        let mesh = Isosurface::new(sphere_field(), 0.0)
            .region([-1.5, -1.5, -1.5], [1.5, 1.5, 1.5])
            .resolution(64)
            .mesh();
        assert!(!mesh.positions.is_empty());
        for p in &mesh.positions {
            assert!(
                (p.length() - 1.0).abs() < 1e-2,
                "vertex off sphere: |p| = {}",
                p.length()
            );
        }
    }

    #[test]
    fn sphere_normals_are_unit_and_outward() {
        let mesh = Isosurface::new(sphere_field(), 0.0)
            .region([-1.5, -1.5, -1.5], [1.5, 1.5, 1.5])
            .resolution(64)
            .mesh();
        for (p, n) in mesh.positions.iter().zip(mesh.normals.iter()) {
            assert!(
                (n.length() - 1.0).abs() < 1e-5,
                "normal not unit: {}",
                n.length()
            );
            assert!(n.dot(*p) > 0.0, "normal not outward: n·p = {}", n.dot(*p));
        }
    }

    #[test]
    fn sphere_euler_characteristic_is_two() {
        let mesh = Isosurface::new(sphere_field(), 0.0)
            .region([-1.5, -1.5, -1.5], [1.5, 1.5, 1.5])
            .resolution(64)
            .mesh();
        let chi = euler_characteristic(&mesh);
        println!("sphere chi = {chi}");
        assert_eq!(chi, 2);
    }

    #[test]
    fn torus_euler_characteristic_is_zero() {
        let mesh = Isosurface::new(torus_field(1.0, 0.35), 0.0)
            .region([-1.6, -1.6, -0.6], [1.6, 1.6, 0.6])
            .resolution(64)
            .mesh();
        let chi = euler_characteristic(&mesh);
        println!("torus chi = {chi}");
        assert_eq!(chi, 0);
    }
}
